		writeUintAttr(wr, itemtag, "sref", "TcoStaWidth", TcoStaWidth);
		writeUintAttr(wr, itemtag, "sref", "TcoKeyWidth", TcoKeyWidth);
		writeUintAttr(wr, itemtag, "sref", "TcoFilWidth", TcoFilWidth);
